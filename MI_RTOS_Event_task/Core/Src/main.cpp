static void CaptureTask(void* parameters)
{
    srand(HAL_GetTick()+7);
    while(1){
    	 ulTaskNotifyTake(pdTRUE, portMAX_DELAY);
    	USART3_write_string("Capturing readings\r\n");
    	for(uint8_t i=0;i<SAMPLE_SIZE;i++){
    		rawData[i]=(rand()%21)+10;

    		vTaskDelay(pdMS_TO_TICKS(100));
    	}
    	 xTaskNotifyGive(calibration_handle);
    }
}

static void CalibrationTask(void* parameters)
{

	 //uint32_t val=10;
	 uint32_t offset=-2;
    while(1){
    	USART3_write_string("Recevied Notification from Capture Task\r\n");
        ulTaskNotifyTake(pdTRUE, portMAX_DELAY);
        USART3_write_string("Calibrating data\r\n");
        for(uint8_t i=0;i<SAMPLE_SIZE;i++){
        	calibration_Data[i]=(rawData[i]*10)+offset;
        }
        xTaskNotifyGive(display_handle);
    }
}

static void DisplayTask(void* parameters)
{
	USART3_write_string("Recevied Notifications from Calibration Task\n");
    while(1){
    	ulTaskNotifyTake(pdTRUE, portMAX_DELAY);
    	for(uint8_t i=0;i<SAMPLE_SIZE;i++){
        USART3_write_string("Samples :");
        USART3_write_number(calibration_Data[i]);
        USART3_write_string("\r\n");
    	}
        xTaskNotifyGive(capture_handle);
    }
}



